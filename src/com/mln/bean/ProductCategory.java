package com.mln.bean;

/**
 * Description 产品种类
 * 2013-4-18
 * @author 11
 */
public class ProductCategory {

	private String id;
	private String name;
	private String pid;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPid() {
		return pid;
	}
	public void setPid(String pid) {
		this.pid = pid;
	}
	
}
